export function getSudokuLocale(language) {
  const value = String(language || '').toLowerCase();
  if (value.startsWith('he')) return 'he';
  if (value.startsWith('ar')) return 'ar';
  return 'en';
}

export function getSudokuText(language, key) {
  const locale = getSudokuLocale(language);
  return sudokuCopy[locale][key] || sudokuCopy.en[key] || key;
}

export function getSudokuLevelLabel(language, level) {
  const label = getSudokuText(language, 'levelLabel');
  return `${label} ${level}`;
}

export function getSudokuHintsLabel(language, count) {
  return `${getSudokuText(language, 'hintsCount')}: ${count}`;
}

export function getSudokuMistakeRuleLabel(language, hasMistakeLimit) {
  return hasMistakeLimit
    ? getSudokuText(language, 'mistakeLimitValue')
    : getSudokuText(language, 'noMistakeLimit');
}

