function normalizeArabicText(value) {
  return String(value || '')
    .trim()
    .replace(/[ًٌٍَُِّْـ]/g, '')
    .replace(/[أإآ]/g, 'ا')
    .replace(/ة/g, 'ه')
    .replace(/ى/g, 'ي')
    .replace(/\s+/g, ' ');
}

export function translateAdminText(value, isHebrew) {
  if (!isHebrew || typeof value !== 'string') return value;
  const direct = he[value];
  if (direct) return direct;
  const compact = value.trim().replace(/\s+/g, ' ');
  if (he[compact]) return he[compact];
  const normalized = normalizeArabicText(compact);
  if (normalizedHe[normalized]) return normalizedHe[normalized];
  for (const [pattern, replacement] of replacements) {
    if (pattern.test(value)) return value.replace(pattern, replacement);
  }
  return value;
}

export function useAdminTranslator() {
  const { i18n } = useTranslation();
  const language = String(i18n.language || '').toLowerCase();
  const isHebrew = language.startsWith('he') || language.startsWith('iw');
  return (value) => translateAdminText(value, isHebrew);
}
